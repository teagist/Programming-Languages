#include "Customer.h"


//****************************************************************************
//	DEFAULT CONSTRUCTOR.   
//****************************************************************************

Customer::Customer()
{
    std::string names[20] = {"John", "Billy", "Joe", "Wayne", "Bobby", "Bill",
	                    	 "Nathan", "Ryan", "Chris", "Ashley", "Michelle",
							 "Mike", "Jerry", "Tom", "Gary", "Mary", "May",
							 "Sarah", "Becky", "Anna"};

	srand(time(0));
    name = names[rand() % 20];
    arrival = (rand()  + 1) % 10;
    departure = (rand()  + 1) % 10;
    service = (rand()  + 1) % 10;
    process = (rand()  + 1) % 10;
}



//****************************************************************************
//	SETTER FOR NAME.
//****************************************************************************

void Customer::setName(std::string newName)
{
    name = newName;
}



//****************************************************************************
//	GETTER FOR NAME.  
//****************************************************************************

std::string Customer::getName() const
{
    return name;
}



//****************************************************************************
//	SETTER FOR ARRIVAL.   
//****************************************************************************

void Customer::setArrival(int newArrival)
{
    arrival = newArrival;
}



//****************************************************************************
//	GETTER FOR ARRIVAL.   
//****************************************************************************

int Customer::getArrival() const
{
    return arrival;
}



//****************************************************************************
//	SETTER FOR DEPARTURE.   
//****************************************************************************

void Customer::setDeparture(int newDeparture)
{
    departure = newDeparture;
}



//****************************************************************************
//	GETTER FOR DEPARTURE.   
//****************************************************************************

int Customer::getDeparture() const
{
    return departure;
}



//****************************************************************************
//	SETTER FOR SERVICE.   
//****************************************************************************

void Customer::setService(int newService)
{
    service = newService;
}



//****************************************************************************
//	GETTER FOR SERVICE.   
//****************************************************************************

int Customer::getService() const
{
    return service;
}



//****************************************************************************
//	SETTER FOR PROCESS.   
//****************************************************************************

void Customer::setProcess(int newProcess)
{
    process = newProcess;
}



//****************************************************************************
//	GETTER FOR PROCESS.   
//****************************************************************************

int Customer::getProcess() const
{
    return process;
}
